  <h1>甚麼是PNG</h1>
  <img src="/files/PNG.png" alt="示例圖片" style="max-width: 100%; height: auto;">
  <p>
    PNG 的全名為可攜式網路圖形 (Portable Network Graphic)，是一種點陣影像檔案。因為 PNG 檔案類型能夠處理有透明或半透明背景的圖形，因此特別受到網頁設計師的喜愛。此檔案格式未申請專利，因此可使用任何影像編輯軟體開啟與編輯 PNG 檔案，而無需取得授權。PNG 檔案的副檔名是 .png，可處理 1,600 萬種色彩，「勢必」 能從眾多競爭者中脫穎而出。
  </p>
  <h1>甚麼是metadata</h1>
    <p>
      Metadata 是用來描述資料的資料，提供有關資料的資訊，例如作者、創建日期、檔案大小、解析度等。Metadata 可以幫助使用者更好地理解和管理資料，並且在搜尋和組織資料時非常有用。
    </p>
